# Python Literals
# Literals can be defined as a data that is given in a variable or constant.

# Python supports the following literals:

# Integer Literals
print(25);
print(-25);
print(100);
print(1_00); # Underscore is ignored
print(0b110101); # Binary to Decimal
print(0o123); # Octal to Decimal
print(0xAB); # Hexadecimal to Decimal

# Float Literals
print(0.0); # 0 is not equal to 0.0
print(-0.0);
print(25.5);
print(10.); # Decimal point is required
print(1_00.5); # Underscore is ignored
print(1e10); # Exponent form of 1 * 10^10
print(6.62607E-34); # Exponent form of 6.62607 * 10^-34

# String Literals
print("Hello World");
print('Hello World');
print("Hello 'World'");
print('Hello "World"');
print("Hello \"World\"");

