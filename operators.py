# Operators

# Arithmetic Operators
# +, -, *, /, %, **, //
print(10 + 3);
print(10 - 3);
print(10 * 3);
print(10 / 3);
print(10 % 3);
print(10 ** 3); # ! Exponent right to left
print(10 // 3); # Floor division

